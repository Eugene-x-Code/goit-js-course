const container = document.querySelector('.list');
const reset = document.querySelector('[type="reset"]');
const form = document.querySelector('.js-form');
const inputs = Array.from(document.querySelectorAll('[type="checkbox"]'));

const source = document.querySelector('.laptops-tpl').innerHTML.trim();
const template = Handlebars.compile(source);

let result = function() {

  const checkedParam = inputs.filter(el => el.checked);

  const param = {
    size: [],
    color: [],
    release_date: [],
  };

  function writeParam() {
    checkedParam.forEach(({name, value}) => {
    
      if(name === 'size') {
        param.size.push(value);
      }else if (name === 'color') {
        param.color.push(value);
      }else{
        param.release_date.push(value);
      }
    }
  )}

  writeParam();

  
const me = laptops.filter(({size, color, release_date}) => {
  const chSize = String(size);
  const chDate = String(release_date);
  
  return param.size.includes(chSize) || param.color.includes(color) || param.release_date.includes(chDate);
});

const markup = template(me);
console.log(markup)
return markup;
};

form.addEventListener('submit', (e) => {
  e.preventDefault();
  container.insertAdjacentHTML('afterbegin', result());
  console.log()
});

reset.addEventListener('click', () => {
  const div = document.querySelectorAll('.item');
  div.forEach(el => el.remove());
}); 